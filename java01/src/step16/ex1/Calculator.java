package step16.ex1;

public class Calculator {
  int result;

  public void plus(int value) { // int result; 값을 처리하기 위해서 메서드 형성. int value 값을 찾아 넣어서 리턴한다.
    result += value;
  }

  public void minus(int value) { // int result; 값을 처리하기 위해서 메서드 형성. int value 값을 찾아 넣어서 리턴한다.
    result -= value;
  }
}